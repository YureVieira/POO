
public class DVD {
	String marca;
	String modelo;
	float peso;
	boolean slim ;
	float preço;
	
	public DVD(String marca, String modelo, float peso, boolean slim,
			float preço) {
		super();
		this.marca = marca;
		this.modelo = modelo;
		this.peso = peso;
		this.slim = slim;
		this.preço = preço;
	}
	
}

